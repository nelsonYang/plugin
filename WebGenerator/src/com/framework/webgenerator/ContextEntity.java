package com.framework.webgenerator;

/**
 *
 * @author nelson
 */
public class ContextEntity {

    private String result;
    private String mappingResult;
    private String rules;
    private String message;

    /**
     * @return the result
     */
    public String getResult() {
        return result;
    }

    /**
     * @param result the result to set
     */
    public void setResult(String result) {
        this.result = result;
    }

    /**
     * @return the mappingResult
     */
    public String getMappingResult() {
        return mappingResult;
    }

    /**
     * @param mappingResult the mappingResult to set
     */
    public void setMappingResult(String mappingResult) {
        this.mappingResult = mappingResult;
    }

    /**
     * @return the rules
     */
    public String getRules() {
        return rules;
    }

    /**
     * @param rules the rules to set
     */
    public void setRules(String rules) {
        this.rules = rules;
    }

    /**
     * @return the message
     */
    public String getMessage() {
        return message;
    }

    /**
     * @param message the message to set
     */
    public void setMessage(String message) {
        this.message = message;
    }
}
